  <hr/>

  <section class="section">
    <p class="intro-text">
      Explore U.S. National Park visitation trends from 1979 to 2023 with this
      web application. Delve into annual and monthly recreation visit data
      across parks, regions, and states using dynamic charts and maps. Analyze
      park rankings over time, compare recreation versus non-recreation visits,
      and uncover trends in camping and backcountry activities.
    </p>
  </section>

